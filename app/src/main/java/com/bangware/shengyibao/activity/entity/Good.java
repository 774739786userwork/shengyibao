public class Good {

    private int img;
    private String goodname;
    private String goodsendtime;
    private String goodtitle;
    private String goodcontent;

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getGoodname() {
        return goodname;
    }

    public void setGoodname(String goodname) {
        this.goodname = goodname;
    }

    public String getGoodsendtime() {
        return goodsendtime;
    }

    public void setGoodsendtime(String goodsendtime) {
        this.goodsendtime = goodsendtime;
    }

    public String getGoodtitle() {
        return goodtitle;
    }

    public void setGoodtitle(String goodtitle) {
        this.goodtitle = goodtitle;
    }

    public String getGoodcontent() {
        return goodcontent;
    }

    public void setGoodcontent(String goodcontent) {
        this.goodcontent = goodcontent;
    }
}
